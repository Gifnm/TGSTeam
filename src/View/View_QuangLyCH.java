    public int getSoNgayTrongThang() {
        Calendar cal = Calendar.getInstance();

        YearMonth yearMonthObject = YearMonth.of(cal.get(Calendar.YEAR), cal.get(Calendar.MONTH) + 1);
        int daysInMonth = yearMonthObject.lengthOfMonth();
        return daysInMonth;
    }

